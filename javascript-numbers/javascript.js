// Example 6 - increment, decrement

// Values arent updated yet in the alert - but will be updated after the line was run
let q = 5;
alert(q++);
let c = 6;
alert(c--);

// This value is updated as its alerted.
let i = 5;
alert(++i);
let o = 6;
alert(--o);

// Wrong float calculation
let f = 0.2 + 0.1;
alert(f);

// Fix by multiplying by 10 and then dividing again.
f = (0.2*10 + 0.1*10) / 10;
alert(f);